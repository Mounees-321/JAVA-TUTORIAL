//2 types : primitive(bool,char,int,flaot,double...) and non primitive(string,array,class,enum,interfaces..)
public class DataTypes {
    public static void main(String[] args) {
        Boolean state1 = false, state2 = true;
        byte b1 = 10, b2 = -10;
        short s1 = 32766;
        int num = 4;
        long big = 1003245L;
        float dec = 24.24F;
        double dd = 2434.23;

        System.out.println("Boolean Values: state1 = " + state1 + ", state2 = " + state2);
        System.out.println("Byte Values: b1 = " + b1 + ", b2 = " + b2);
        System.out.println("Short Value: s1 = " + s1);
        System.out.println("Integer Value: num = " + num);
        System.out.println("Long Value: big = " + big);
        System.out.println("Float Value: dec = " + dec);
        System.out.println("Double Value: dd = " + dd );
    }
}

